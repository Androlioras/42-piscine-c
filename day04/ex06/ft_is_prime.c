int	ft_is_prime(int nb)
{
	int i;
	int result;

	result = 1;
	i = 2;
	while (i <= nb / 2)
	{
		if (nb % i == 0)
			result = 0;
		i++;
	}
	if (nb == 1 || nb == 0 || nb < 0)
		result = 0;
	return (result);
}
