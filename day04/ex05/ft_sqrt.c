int	ft_sqrt(int nb)
{
	int i;
	int sqrt;

	i = 1;
	sqrt = 0;
	while (i * i <= nb)
	{
		if (i * i == nb)
			sqrt = i;
		i++;
	}
	return (sqrt);
}
