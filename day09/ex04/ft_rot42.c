char	*ft_rot42(char *str)
{
	int i;

	i = 0;
	while (str[i])
	{
		if (str[i] > 64 && str[i] < 91)
			str[i] = 64 + (str[i] - 22) % 26;
		if (str[i] > 96 && str[i] < 123)
			str[i] = 96 + (str[i] - 54) % 26;
		i++;
	}
	return (str);
}
