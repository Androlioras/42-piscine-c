int		ft_bouton(int i, int j, int k)
{
	int mid;

	mid = 0;
	if ((j > i && i > k) || (k > i && i > j))
		mid = i;
	if ((k > j && j > i) || (i > j && j > k))
		mid = j;
	if ((i > k && k > j) || (j > k && k > i))
		mid = k;
	return (mid);
}
