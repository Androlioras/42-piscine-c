void	ft_putchar(char c);

void	ft_putnbr(int nb)
{
	int i[3];

	i[1] = 1;
	i[2] = 0;
	if (nb >= -2147483648 && nb <= 2147483647)
	{
		if (nb < 0)
		{
			ft_putchar('-');
			if (nb == -2147483648)
				i[2] = 1;
			nb = -nb - i[2];
		}
		i[0] = nb;
		while (nb / i[1] > 9)
			i[1] *= 10;
		while (i[1] != 1)
		{
			nb = i[0];
			ft_putchar(48 + nb / i[1]);
			i[0] = i[0] - (nb / i[1]) * i[1];
			i[1] /= 10;
		}
		ft_putchar(i[0] + 48 + i[2]);
	}
}
