int		ft_strcmp(char *s1, char *s2)
{
	int i[3];

	i[0] = 0;
	i[2] = 0;
	while (s1[i[0]] != '\0' && s2[i[0]] != '\0' && i[2] == 0)
	{
		if (s1[i[0]] < s2[i[0]])
			i[2] = 1;
		else if (s1[i[0]] > s2[i[0]])
			i[2] = 1;
		i[0]++;
	}
	if (i[2] == 1)
	{
		i[0]--;
		i[1] = s1[i[0]] - s2[i[0]];
	}
	else if (s1[i[0]] == '\0' && s2[i[0]] == '\0')
		i[1] = 0;
	else if (s1[i[0]] == '\0')
		i[1] = -s2[i[0]];
	else
		i[1] = s1[i[0]];
	return (i[1]);
}
