int		ft_str_is_alpha(char *str)
{
	int i;
	int result;

	i = 0;
	result = 1;
	while (str[i] != '\0')
	{
		if (str[i] < 65 || str[i] > 122)
			result = 0;
		if (str[i] > 91 && str[i] < 96)
			result = 0;
		i++;
	}
	return (result);
}
